using System.Threading.Tasks;
using Xunit;

namespace Xhttp.Tests.Features.HttpMethods;

public class OptionsTests : TestBase
{
    [Fact]
    public async Task Generator_GeneratesOptionsRequestsCorrectly()
    {
        var source = CSharp(
            """
            using Xhttp;
            using System.Threading.Tasks;

            namespace X;

            [Client]
            public interface ICustomClient
            {
                [Options("api")]
                Task<string> GetApiOptions();
            }
            """);
        var expectedOutput = CSharp(
            """
            // <auto-generated/>
            using System.Net.Http.Json;

            namespace X
            {
                [global::System.CodeDom.Compiler.GeneratedCode("Xhttp", "0.1.0")]
                public sealed class CustomClient : ICustomClient
                {
